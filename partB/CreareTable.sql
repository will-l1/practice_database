FOREIGN KEY(movie_genre) REFERENCES genre(genre_name),
FOREIGN KEY(movie_country) REFERENCES country(country_name),
FOREIGN KEY(movie_main_actor) REFERENCES actor(actor_name),
FOREIGN KEY(production_company) REFERENCES company(company_name)
